/*
This challenge requires you to return the string "true" if the second integer parameter (num2) is larger than the
first (num1). This is actually a very simple challenge which doesn't require a lot of code.*/

function CheckNums(num1, num2){
    return (num1 === num2) ? "-1" : (num2 > num1) ? true : false;
}